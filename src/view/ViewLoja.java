    private void jbExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbExcluirActionPerformed
        int linha = jtLoja.getSelectedRow();
        int codigoLoja = (int) jtLoja.getValueAt(linha, 0);
        
        try {
            if(controllerLoja.excluirLojaController(codigoLoja)){
            JOptionPane.showMessageDialog(this, "Loja excluída com sucesso", "Atenção", JOptionPane.INFORMATION_MESSAGE);
            carregarLoja();
            }else{
                JOptionPane.showMessageDialog(this, "Erro ao excluir loja","Erro", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException ex) {
            Logger.getLogger(ViewLoja.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Erro ao excluir loja","Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jbExcluirActionPerformed

    private void jbAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAlterarActionPerformed
        salvarAlterar = "alterar";
        int linha = jtLoja.getSelectedRow();

        try{
            int codigoLoja = (int) jtLoja.getValueAt(linha, 0);
            //setar dados do banco
            modelLoja = controllerLoja.retornarLojaController(codigoLoja);
            //setar na interface
            this.jtfCodigo.setText(String.valueOf(modelLoja.getLojId()));
            this.jtfSigla.setText(modelLoja.getLojSigla());
            this.jtfCidade.setText(modelLoja.getLojCidade());

        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Erro ao encontrar usuário","Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jbAlterarActionPerformed

