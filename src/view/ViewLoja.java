    private void carregarLoja(){
        listaModelLoja = controllerLoja.retornarListaLojaController();
        DefaultTableModel modelo = (DefaultTableModel) jtLoja.getModel();
        modelo.setNumRows(0);
        //inserindo
        int cont = listaModelLoja.size();
        for(int i=0; i < cont; i++){
            modelo.addRow(new Object[]{
                listaModelLoja.get(i).getLojId(),
                listaModelLoja.get(i).getLojSigla(),
                listaModelLoja.get(i).getLojCidade()
            });
        }
    }
    
    private void limparFormulario(){
        jtfCodigo.setText("");
        jtfSigla.setText("");
        jtfCidade.setText("");
    }
    
    private void salvarLoja (){
       modelLoja = new ModelLoja();
       modelLoja.setLojId(Integer.valueOf(jtfCodigo.getText()));
       modelLoja.setLojSigla(jtfSigla.getText());
       modelLoja.setLojCidade(jtfCidade.getText());
       if (controllerLoja.salvarLojaController(modelLoja)){
       //cadastrou usuario
       limparFormulario();
       JOptionPane.showMessageDialog(this, "Loja cadastrada com sucesso", "Atenção", JOptionPane.INFORMATION_MESSAGE);
       carregarLoja();     
       }else{
       //nao cadastrou    
       JOptionPane.showMessageDialog(this, "Erro ao cadastrar loja","Erro", JOptionPane.ERROR_MESSAGE);
       
       }
    }
    
    private void alterarLoja (){
        
       modelLoja = new ModelLoja();
       modelLoja.setLojId(Integer.valueOf(jtfCodigo.getText()));
       modelLoja.setLojSigla(jtfSigla.getText());
       modelLoja.setLojCidade(jtfCidade.getText());
       if (controllerLoja.alterarLojaController(modelLoja)){
       //cadastrou usuario
       limparFormulario();
       JOptionPane.showMessageDialog(this, "Loja alterado com sucesso", "Atenção", JOptionPane.INFORMATION_MESSAGE);
       carregarLoja();     
       }else{
       //nao cadastrou    
       JOptionPane.showMessageDialog(this, "Erro ao alterar loja","Erro", JOptionPane.ERROR_MESSAGE);
       
       }
    }
    

